package com.yunfan.spring.ioc.overview.domain;

/**
 * @author Administrator
 * @description 用户类
 * @create 2020/11/9 11:14
 * @since 1.0.0
 */
public class User {

    private Long id;

    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
